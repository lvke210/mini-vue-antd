      stairs in the neighborhood of Pell Street, and peered about for a long
      time on the dime1 landing before he found the name he wanted written
      obscurely on one of the doors. He pushed open this door, as he had been
      told to do, and found himself in a tiny room, which contained no furniture
      but a plain kitchen table, a rocking-chair, and an ordinary chair. On one
      of the dirty buff-coloured walls were a couple of shelves, containing in
      all perhaps a dozen bottles and jars. An old man sat in the rocking-chair,
      reading a newspaper. Alan, without a word, handed him the card he had been
      given. "Sit down, Mr. Austen," said the old man very politely. "I am glad
      to make your acquaintance." "Is it true," asked Alan, "that you have a
      certain mixture that has-er-quite extraordinary effects?" "My dear sir,"
      replied the old man, "my stock in trade is not very large-I don't deal in
      laxatives and teething mixtures-but such as it is, it is varied2. I think
      nothing I sell has effects which could be precisely3 described as
      ordinary." "Well, the fact is. . ." began Alan. "Here, for example,"
      interrupted the old man, reaching for a bottle from the shelf. "Here is a
      liquid as colourless as water, almost tasteless, quite imperceptible in
      coffee, wine, or any other beverage4. It is also quite imperceptible to
      any known method of autopsy5." "Do you mean it is a poison?" cried Alan,
      very much horrified6. "Call it a glove-cleaner if you like," said the old
      man indifferently. "Maybe it will clean gloves. I have never tried. One
      might call it a life-cleaner. Lives need cleaning sometimes." "I want
      nothing of that sort," said Alan. "Probably it is just as well," said the
      old man. "Do you know the price of this? For one teaspoonful7, which is
      sufficient, I ask five thousand dollars. Never less. Not a penny less." "I
      hope all your mixtures are not as expensive," said Alan apprehensively8.
      "Oh dear, no," said the old man. "It would be no good charging that sort
